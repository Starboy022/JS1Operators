//Arithemetic Operators
let a=50,b=10;
console.log("Arithemetic Operators");
console.log("a+b: "+ (a+b));
console.log("a-b: "+ (a-b));
console.log("a*b: "+ (a*b));
console.log("a/b: "+ (a/b));
console.log("a%b: "+ (a%b));
//Relational Operators
let c=10,d=20;
console.log("Relational Operators");
console.log("c==d?: "+(c===d));
console.log("c!=d?: "+(c!==d));
console.log("c<d?: "+(c<d));
console.log("c>d?: "+(c>d));
console.log("c<=d?: "+(c<=d));
console.log("c>=d?: "+(c>=d));

//logical Operators
let x=10,y=20;
console.log("Logical Operators");
console.log("(x<y)&&(y>x): "+((x<y)&&(y>x)));
console.log("(x>y)||(y>x): "+((x>y)||(y>x)));

